public class Ejemplodos {
    public static void main(String[] args) {
        int[] numeros = {2, 4, 6, 1, 3, 5};
        for (int i = 0; i < numeros.length; i++) {
            int numbers = numeros[i];
            if (numbers % 2 == 0) {
                System.out.println("Los numeros par son " + numbers);
            }

        }
    }


}



